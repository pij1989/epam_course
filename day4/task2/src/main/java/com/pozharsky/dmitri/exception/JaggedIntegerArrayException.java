package com.pozharsky.dmitri.exception;

public class JaggedIntegerArrayException extends Exception {
    public JaggedIntegerArrayException() {
        super();
    }

    public JaggedIntegerArrayException(String message) {
        super(message);
    }

    public JaggedIntegerArrayException(String message, Throwable cause) {
        super(message, cause);
    }

    public JaggedIntegerArrayException(Throwable cause) {
        super(cause);
    }
}
